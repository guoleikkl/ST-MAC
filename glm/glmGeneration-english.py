def convert_to_custom_json_format(data):
    """
    将给定的实体和关系数据转换为指定的 JSON 格式
    """
    result = {
        "id": 994,  # 可以根据需要生成唯一ID
        "kbs": {},
        "text": [
            "教育部办公厅发布《关于加强中小学人工智能教育的通知》，明确2030年前在中小学基本普及人工智能教育。张女士期待AI教育。王鹏理解家长希望孩子早些了解AI的用心。《通知》指出以人工智能引领构建以人为本的创新教育生态，并鼓励将AI教育纳入课后服务项目和研学实践。张新源认为AI教育能激发学习兴趣，提高学习效率。韩国政府宣布普及人工智能数字教科书，引发对学生对数字设备依赖的担忧。"
        ]
    }
    
    # 处理关系
    for idx, relation in enumerate(data['关系']):
        # 创建唯一的三元组ID
        triple_id = f"W{idx+1:03d}"  # 生成类似 W001, W002 的ID
        
        # 创建三元组
        result["kbs"][triple_id] = [
            relation['头实体'],  # 头实体
            relation['头实体'],  # 头实体（重复，保持与示例格式一致）
            [
                [
                    relation['关系'],  # 关系
                    relation['尾实体']  # 尾实体
                ]
            ]
        ]
    
    return result

